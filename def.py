def addition(number1,number2):
    total = number1 + number2
    print(number2,'+',number1,'=',total)

addition(10,12)
def substraction(number1,number2):
    total = number1 - number2
    print(number1,'-',number2,'=',total)

substraction(58,34)
def multiplication(number1,number2):
    total = number1 * number2
    print(number2,'x',number1,'=',total)

multiplication(6,7)
def division(number1,number2):
    total = number1//number2
    print(number1,':',number2,'=',total)

division(144,12)

def increment(number1,number2):
    total = number1 + 1
    print(number1,'+','1','=',total)

increment(12,1)

def decrement(number1,number2):
    total = number1 - 1
    print(number1,'-','1','=',total)

decrement(12,1)
